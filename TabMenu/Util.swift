import UIKit

func getColor(red:CGFloat,green:CGFloat,blue:CGFloat,alpha:CGFloat = 1.0)->UIColor{
    
   return UIColor(red: red/255.0, green: green/255.0, blue: blue/255.0, alpha: 1.0)
}